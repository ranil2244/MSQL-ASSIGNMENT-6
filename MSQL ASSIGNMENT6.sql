#LEFT JOIN
SELECT COUNTRY.COUNTRY_ID,PERSONS.COUNTRY_ID, 
PERSONS.POPULATION,PERSONS.COUNTRY_NAME,PERSONS.
RATING FROM PERSONS LEFT JOIN COUNTRY ON PERSONS.COUNTRY_ID = COUNTRY.COUNTRY_ID;

#RIGHT JOIN
SELECT COUNTRY.COUNTRY_ID,PERSONS.COUNTRY_ID, 
PERSONS.POPULATION,PERSONS.COUNTRY_NAME,PERSONS.
RATING FROM PERSONS RIGHT JOIN COUNTRY ON PERSONS.COUNTRY_ID = COUNTRY.COUNTRY_ID;


